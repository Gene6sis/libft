#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	int		i;
	char		*dest;

	size = 0;
	while (s1[size])
		size++;
	dest = malloc(sizeof(*s1) * (size + 1));
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
